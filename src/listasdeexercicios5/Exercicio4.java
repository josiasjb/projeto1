package listasdeexercicios5;

import java.util.Scanner;

/*
 * Faça um programa para armazenar em uma matriz os
compromissos de uma agenda pessoal. Cada dia do mês deve
conter 24 horas, onde para cada uma destas 24 horas podemos
associar um tarefa específica (compromisso agendado). O
programa deve ter um menu onde o usuário indica o dia do mês
que deseja alterar e a hora, entrando em seguida com o
compromisso, ou então, o usuário pode também consultar a
agenda, fornecendo o dia e a hora para obter o
compromisso armazenado.
 */
public class Exercicio4 {

	public static void main(String[] args) {

		Scanner scan  = new Scanner (System.in);
		
		String[][] compromissos = new String[31][24];
		
		boolean sair = false;
		byte opçao;
		while (!sair){
			
			System.out.println("Digite 1 para adicionar compromisso.");
			System.out.println("Digite 2 para verificar compromisso.");
			System.out.println("Digite 0 para sair.");
			
			opçao = scan.nextByte();
			
			if (opçao == 1){//acionar compromisso
				
				boolean diaValido = false;
				int dia = 0;
				while (!diaValido){
					System.out.println("Entre com o dia do mês");
					dia = scan.nextInt();
					if (dia > 0 && dia <= 31){
						diaValido = true;
					} else {
						System.out.println("Dia inválido, digite novamente");
					}
				}
				
				boolean horaValida = false;
				int hora = 0;
				while (!horaValida){
					System.out.println("Entre com a hora do mês");
					hora = scan.nextInt();
					if (hora >= 0 && hora <= 24){
						horaValida = true;
					} else {
						System.out.println("Hora inválido, digite novamente");
					}
				}
				
				dia--;
				System.out.println("Digite compromisso");
				compromissos[dia][hora] = scan.next();
				
			} else if (opçao == 2){//verificar compromisso
				
				boolean diaValido = false;
				int dia = 0;
				while (!diaValido){
					System.out.println("Entre com o dia do mês");
					dia = scan.nextInt();
					if (dia > 0 && dia <= 31){
						diaValido = true;
					} else {
						System.out.println("Dia inválido, digite novamente");
					}
				}
				
				boolean horaValida = false;
				int hora = 0;
				while (!horaValida){
					System.out.println("Entre com a hora do mês");
					hora = scan.nextInt();
					if (hora >= 0 && hora <= 24){
						horaValida = true;
					} else {
						System.out.println("Hora inválido, digite novamente");
					}
				}
				
				dia--;
				System.out.println("O compromisso agendado e:");
				System.out.println(compromissos[dia][hora]);
				
			} else if (opçao == 0){
				sair = true;
			} else {
				System.out.println("Opção inválida, digite novamente");
			}
		}
		
	}

}
